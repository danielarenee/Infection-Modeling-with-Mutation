"""
This script reads the csv file generated by sircm_3d_hopf_surface_data.jl
and plots the 2D Hopf bifurcation curves as a 3D surface.
"""

import os
import pandas as pd
import numpy as np
from scipy.interpolate import interp1d
import plotly.graph_objects as go

# Paths
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
CSV_PATH = os.path.join(SCRIPT_DIR, "hopf_surface_eps_beta_sigma.csv")
HTML_PATH = os.path.join(SCRIPT_DIR, "hopf_surface_eps_beta_sigma.html")

# Load the bifurcation data generated with BifurcationKit
df = pd.read_csv(CSV_PATH)

# Setup a grid for tilde_eps
eps_lin = np.linspace(df["tilde_eps"].min(), df["tilde_eps"].max(), 150)
sigmas = sorted(df["sigma"].unique())

# Initialize 2D grids for the upper and lower sheets 
upper_grid = np.full((len(sigmas), len(eps_lin)), np.nan)
lower_grid = np.full((len(sigmas), len(eps_lin)), np.nan)

# Iterate over each unique sigma value
for i, sig in enumerate(sigmas):
    group = df[df["sigma"] == sig]
    if len(group) < 5:
        continue
    
    # Identify the vertex (minimum epsilon) where the upper and lower branches meet
    min_row = group.loc[group["tilde_eps"].idxmin()]
    eps_vertex = min_row["tilde_eps"]
    beta_vertex = min_row["beta0"]
    
    # Split into upper and lower branches
    forward_pts = group[group["branch"] == "forward"].sort_values("tilde_eps")
    backward_pts = group[group["branch"] == "backward"]
    b_seq = backward_pts.reset_index(drop=True)
    v_idx = b_seq["tilde_eps"].idxmin()
    
    b_upper = b_seq.iloc[:v_idx+1]
    b_lower = b_seq.iloc[v_idx:]
    
    # Combine and sort upper points (going from vertex up to beta0 = 2000)
    upper_combined = pd.concat([b_upper, forward_pts]).sort_values("tilde_eps").drop_duplicates(subset=["tilde_eps"])
    
    # Sort lower points (going from vertex rightward to tilde_eps = 2.0)
    lower_combined = b_lower.sort_values("tilde_eps").drop_duplicates(subset=["tilde_eps"])
    
    # 1D interpolation of the data points along the slice
    if len(upper_combined) >= 2:
        f_upper = interp1d(upper_combined["tilde_eps"], upper_combined["beta0"], bounds_error=False, fill_value=np.nan)
        upper_grid[i, :] = f_upper(eps_lin)
        
    if len(lower_combined) >= 2:
        f_lower = interp1d(lower_combined["tilde_eps"], lower_combined["beta0"], bounds_error=False, fill_value=np.nan)
        lower_grid[i, :] = f_lower(eps_lin)

# Assemble 3D meshgrid
eps_mesh, sig_mesh = np.meshgrid(eps_lin, sigmas)
fig = go.Figure()

# Plot the upper surface
fig.add_trace(go.Surface(
    x=eps_mesh, y=sig_mesh, z=upper_grid,
    colorscale="Blues", opacity=0.75,
    name="Upper branch (surface)",
    showscale=False
))

# Plot the lower surface
fig.add_trace(go.Surface(
    x=eps_mesh, y=sig_mesh, z=lower_grid,
    colorscale="Reds", opacity=0.75,
    name="Lower branch (surface)",
    showscale=False
))

# Overlay raw coordinates
fig.add_trace(go.Scatter3d(
    x=df["tilde_eps"], y=df["sigma"], z=df["beta0"],
    mode="markers", marker=dict(size=1.5, color="black"),
    name="Raw points"
))

# Layout
fig.update_layout(
    scene=dict(
        xaxis_title="Mutation feedback ε̃",
        yaxis_title="Cross-immunity factor σ",
        zaxis_title="Contact rate β0",
        camera=dict(eye=dict(x=1.8, y=1.8, z=1.2))
    ),
    title="SIRCmw Hopf bifurcation surface ",
    width=950, height=750
)

# Export interactive visualization
fig.write_html(HTML_PATH)
fig.show()